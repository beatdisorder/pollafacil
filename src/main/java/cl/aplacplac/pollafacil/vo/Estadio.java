package cl.aplacplac.pollafacil.vo;

public class Estadio {
	private String nombre;

	public Estadio() {
	}

	public Estadio(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre
	 *            the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
